

public class Relational {
    
    public static void main(String[] args) {
        
        int a = 10;
        int b = 5;

        System.out.println("For Given A = " + a + ", B = " + b);

        System.out.println("A == B -> " + (a == b));
        System.out.println("A != B -> " + (a != b));
        System.out.println("A > B  -> " + (a > b));
        System.out.println("A < B  -> " + (a < b));
        System.out.println("A >= B -> " + (a >= b));
        System.out.println("A <= B -> " + (a <= b));
    }
}
